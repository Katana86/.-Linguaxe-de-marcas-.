public class Ejercicio_DoWhile {
    public static void main(String[] args){
        int x;
        System.out.println("Bucle do while\n");
        x=20;
        do{
            System.out.println(x);
            x++;
        }while (x<10);
//-----------------------------------------------------------//
        System.out.println("\n\nBucle while\n");
        x=20;
        while(x<10)
        {
            System.out.println(x);
            x++;
        }
    }
}
